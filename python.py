# operators
# Operators
# Assignment operators
x = 5
print(x)  
y = 5
y += 3
print(y)  
z = 5
z -= 3
print(z) 
# Arithmetic operators
a = 5 + 5
print(a)  
b = 5 - 3
print(b) 
c = 5 * 6
print(c)  
d = 18 / 5
print(d) 
e = 29 % 6
print(e) 
f = 4 ** 5
print(f) 
# Comparison operators
x = 9
y = 7
print(x == y) 
print(x != y)  
print(x < y)   
print(x > y)   
print(x <= y) 
print(x >= y)  
# Logical operators
a = False
b = True
print(a and b)  
print(a or b)   
print(not a)    
print(not b)   
# Bitwise operators
x = 0b1010
y = 0b1100

print(bin(x & y)) 
print(bin(x | y))  
print(bin(x ^ y))  
print(bin(x << 2))
print(bin(y >> 1)) 


